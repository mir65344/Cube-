    selectRandomQuestions(testType) {
    const allQuestions = this.allQuestions[testType];
    const limit = this.questionLimit[testType];
    
    if (!allQuestions || allQuestions.length === 0) {
        console.error('–ù–µ—Ç –≤–æ–ø—Ä–æ—Å–æ–≤ –¥–ª—è —Ç–µ—Å—Ç–∞:', testType);
        return [];
    }
    
    // –ï—Å–ª–∏ –≤–æ–ø—Ä–æ—Å–æ–≤ –º–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ –ª–∏–º–∏—Ç—É - –ø—Ä–æ—Å—Ç–æ –ø–µ—Ä–µ–º–µ—à–∏–≤–∞–µ–º –≤—Å–µ –≤–æ–ø—Ä–æ—Å—ã
    if (allQuestions.length <= limit) {
        return [...allQuestions].sort(() => Math.random() - 0.5);
    }
    
    // –ò—Å–ø–æ–ª—å–∑—É–µ–º Set –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è —É–∂–µ –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö –≤–æ–ø—Ä–æ—Å–æ–≤
    const selectedIndices = new Set();
    const result = [];
    
    // –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ –≤–æ–ø—Ä–æ—Å—ã
    while (result.length < limit) {
        const randomIndex = Math.floor(Math.random() * allQuestions.length);
        
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —ç—Ç–æ—Ç –≤–æ–ø—Ä–æ—Å –µ—â–µ –Ω–µ –≤—ã–±—Ä–∞–Ω
        if (!selectedIndices.has(randomIndex)) {
            selectedIndices.add(randomIndex);
            result.push(allQuestions[randomIndex]);
        }
        
        // –ó–∞—â–∏—Ç–∞ –æ—Ç –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ–≥–æ —Ü–∏–∫–ª–∞ (–Ω–∞ –≤—Å—è–∫–∏–π —Å–ª—É—á–∞–π)
        if (selectedIndices.size >= allQuestions.length) {
            break; // –í—Å–µ –≤–æ–ø—Ä–æ—Å—ã —É–∂–µ –≤—ã–±—Ä–∞–Ω—ã
        }
    }
    
    return result;
}

